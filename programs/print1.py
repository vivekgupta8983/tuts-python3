'''
print() with replacement operator {}
'''

name = "Vivek Gupta"
salary = "100000"
GirlFriend = "Sunny Leona"

print("Hello {0} your salary is {1} and Your Friend {2} is waiting".format(
    name, salary, GirlFriend))
print("Hello {x} your salary is {y} and Your Friend {z} is waiting".format(
    x=name, y=salary, z=GirlFriend))
